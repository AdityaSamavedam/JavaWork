package Basics;

public class Casting {
    public static void main(String[] args) {
        double value1 = 5.7;
        double value2 = 5.9;

        int roundedValue1 = (int) (value1 + 0.5); // Rounds to 6
        int roundedValue2 = (int) (value2); // Rounds to -5

        System.out.println("Rounded Value 1: " + roundedValue1);
        System.out.println("Rounded Value 2: " + roundedValue2);
        System.out.println("It\'s a sunny day.");
        System.out.println("Single Quote: 'Java'");
       

    }
}

